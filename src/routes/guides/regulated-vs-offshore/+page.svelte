    <h2 class="text-2xl font-bold text-white mb-5 border-b border-white/10 pb-4">The Risks of Offshore Platforms</h2>
    <p class="text-[#94A3B8] mb-6 leading-relaxed">Offshore platforms operate in a "gray market." While they may accept Canadian players, they are not beholden to provincial regulatory bodies like the <strong class="text-white">AGCO</strong> (Alcohol and Gaming Commission of Ontario) or the <strong class="text-white">iGO</strong> (iGaming Ontario). This lack of oversight poses severe risks to your funds and personal data.</p>

    <div class="grid grid-cols-1 md:grid-cols-2 gap-6 mb-8">
      <div class="navy-card rounded-2xl p-6 border border-white/10">
