        <h3 class="text-xl font-bold text-white mb-2">No Guaranteed Payouts</h3>
        <p class="text-gray-400 text-sm leading-relaxed">Unregulated sites can withhold your winnings without consequence. You have no legal recourse within Canada if an offshore operator refuses to process your withdrawal.</p>
      </div>
      <div class="navy-card rounded-2xl p-6 border border-white/10">
        <div class="text-red-400 text-2xl mb-4">⚠</div>
        <h3 class="text-xl font-bold text-white mb-2">Data Privacy Breaches</h3>
        <p class="text-gray-400 text-sm leading-relaxed">Your KYC (Know Your Customer) documents and payment details are governed by the laws of foreign jurisdictions, which often fall short of Canadian privacy standards.</p>
      </div>
      <div class="navy-card rounded-2xl p-6 border border-white/10">
        <div class="text-red-400 text-2xl mb-4">⚠</div>
        <h3 class="text-xl font-bold text-white mb-2">No RNG Certification</h3>
        <p class="text-gray-400 text-sm leading-relaxed">Game fairness is not independently verified at offshore sites. Regulated Ontario casinos require certified RNG testing from approved labs like eCOGRA or GLI.</p>
      </div>
      <div class="navy-card rounded-2xl p-6 border border-white/10">
        <div class="text-red-400 text-2xl mb-4">⚠</div>
        <h3 class="text-xl font-bold text-white mb-2">No Responsible Gambling Tools</h3>
        <p class="text-gray-400 text-sm leading-relaxed">Offshore platforms are not required to provide deposit limits, self-exclusion, or cooling-off periods — protections that are mandatory at AGCO-licensed operators.</p>
      </div>
